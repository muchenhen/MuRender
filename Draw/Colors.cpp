float GetFloatRand()
{
	union FloatRand r;

	r.BitArea.Signed = 0;
	r.BitArea.Exp = 1;
	r.BitArea.Frac = (rand() * rand()) % 0x800000;
	if (r.BitArea.Frac == 0x7FFFFF)
		r.BitArea.Exp = 0x7D;
	else if (r.BitArea.Frac == 0)
		r.BitArea.Exp = 0x7E;
	else
		r.BitArea.Exp = 0x7E;

	return r.Value;
}

MuVector randColor()
{
	float r, g, b = 1.0f;
	r = GetFloatRand();
	g = GetFloatRand();
	b = GetFloatRand();
	MuVector color(r, g, b, 1.0f);
	return color;
}
